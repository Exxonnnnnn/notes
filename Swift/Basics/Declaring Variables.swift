There are two keywords you can use to declare a variable or constant in swift. These are "let"
and "var". The syntax for this would be:

var name = "Exxon";
let name = "Exxon";

Unlike JavaScript, when using the "let" keyword in swift you are declaring a constant value. This
means you cannot change it after giving it an initial value. When using the "var" keyword you are 
declaring a variable value meaning you can change its value.

In the example below we will declare a variable and a constant (Age and Name).
*/

