"""
Logical operators are used to combine conditional statements

Documentation - https://www.w3schools.com/python/python_operators.asp
"""

x = 3
y = 5

x < y and x < y # Returns true if both statements are true
x < y or x < y # Returns true if one of the statements is true
not(x < y and x < y) # Reverse the result, returns false if the result is true