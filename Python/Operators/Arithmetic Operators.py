"""
Arithmetic operators are used with numeric values to perform common 
mathematical operations.

Documentation - https://www.w3schools.com/python/python_operators.asp
"""

x = 3
y = 5

x + y # Adds the value of x and y (Addition, 8)
x - y # Subtracts the value of y from x (Subtraction, -2)
x * y # Multiplies x by y (Multiplication, 15)
x / y # Divides 3 by 5 (Division, 0.6)
x % y # Gets the remainder of from when you divide x by y (Modulus, 3)
x ** y # Raises x by the power of y (Exponentiation, 243)
x // y # Returns the largest integer less than or equal to the normal division result of x divided by y (Floor Division, 0)